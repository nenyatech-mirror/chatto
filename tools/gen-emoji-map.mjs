#!/usr/bin/env node

/**
 * Generates cli/internal/core/emoji_gen.go from the gemoji npm package.
 * This ensures the Go backend uses the same emoji data as the frontend,
 * preventing version mismatches where newer emojis work in the picker
 * but fail validation on the backend.
 *
 * Run via: mise codegen-emoji
 */

import { createRequire } from 'node:module';
import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Resolve gemoji from the frontend workspace package.
const require = createRequire(resolve(__dirname, '../apps/frontend/package.json'));
const { gemoji } = require('gemoji');

const outputPath = resolve(__dirname, '../cli/internal/core/emoji_gen.go');

// Build name → emoji entries (each emoji can have multiple names/aliases)
const entries = [];
for (const e of gemoji) {
	for (const name of e.names) {
		entries.push([name, e.emoji]);
	}
}

// Sort for deterministic output
entries.sort((a, b) => a[0].localeCompare(b[0]));

// Generate Go source
const goEntries = entries.map(([name, emoji]) => {
	// Escape backslashes and quotes in name (shouldn't occur in practice)
	const safeName = name.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
	return `\t"${safeName}": "${emoji}",`;
});

const goSource = `// Code generated by tools/gen-emoji-map.mjs from gemoji. DO NOT EDIT.

package core

// emojiNameToUnicode maps emoji shortcode names to their Unicode representations.
// Generated from the gemoji npm package (GitHub's emoji dataset).
// Total entries: ${entries.length} (including aliases)
var emojiNameToUnicode = map[string]string{
${goEntries.join('\n')}
}
`;

writeFileSync(outputPath, goSource);
console.log(`Generated ${outputPath} with ${entries.length} emoji entries`);
